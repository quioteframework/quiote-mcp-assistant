    /**
     * The "project-aware" tools -- only registered when `bin/quiote-assistant`
     * was launched with `--target-app-dir`, so a plain knowledge-only server
     * (the common case) doesn't advertise tools that would only ever fail
     * with "no target app configured." The read-only ones (`project_info`
     * through `read_config`) never write; the ones that can (`scaffold_*`,
     * `run_console`) only ever *create new files* (never overwrite -- see
     * `ScaffoldWriter`) and default `dry_run` to `true`. Every one delegates to
     * {@see \QuioteMcpAssistant\Mcp\Introspection\TargetAppIntrospector} (or,
     * for `run_console`, `ConsoleRunner`), which runs an isolated subprocess
     * against the target app -- required because `Quiote\Config\Config`/
     * `Context`/`PluginManager` are static, process-wide singletons that lock
     * the bootstrapped app's `core.app_dir` readonly, so this
     * (already-bootstrapped) assistant cannot also bootstrap an arbitrary
     * second app in the same process.
     */
