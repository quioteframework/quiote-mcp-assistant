/**
 * The Quiote Assistant MCP server -- knowledge tools/resources/prompts plus,
 * when a target app is configured, project-aware introspection and
 * scaffolding tools.
 *
 * Because the framework has *no* attribute discovery for plain
 * `#[McpResource]`/`#[McpPrompt]`/`#[McpTool]` classes (only `#[Route]` actions
 * carrying `#[McpTool]` are scanned), every capability this app
 * exposes is registered *manually* here, directly against {@see McpCatalog}.
 *
 * Historical note: `PluginRegistrar::mcpTool()`/`mcpResource()`/`mcpPrompt()`
 * convenience wrappers used to exist on core's `PluginRegistrar` and forwarded
 * to `McpCatalog`; the framework's `quioteframework/mcp` monorepo split
 * removed them from core (core can no longer depend on the now-optional MCP
 * package) without replacing them with anything on the mcp package's side.
 * `$this->mcpTool()`/`mcpResource()`/`mcpPrompt()` below reproduce that exact
 * former behavior locally -- one `[handlerFqcn, method]` pair translation,
 * kept in one place -- so every call site below is otherwise unchanged.
 *
 * Add this class (and `Quiote\Mcp\McpPlugin`, from the separate
 * `quioteframework/mcp` package) to the `plugins` config key and set
 * `mcp.enabled = true` to serve it over stdio via `mcp:serve` /
 * `bin/quiote-assistant`.
 */
final class AssistantPlugin implements PluginInterface
{
    public function name(): string
    {
        return 'quiote/assistant';
    }

    public function register(PluginRegistrar $registrar): void
    {
        // Sensible knowledge-server identity (app settings.php still wins).
        $registrar->configDefault('mcp.server_name', 'quiote-assistant');
        $registrar->configDefault('mcp.server_version', '0.1.0');

        // The re-runnable docs bundler.
        $registrar->command(DocsSyncCommand::class);

        $this->registerDocResources();
        $this->registerTools();
        $this->registerPrompts();
        $this->registerProjectTools();
    }

    /**
     * One MCP resource per bundled doc, all served by the single
     * {@see DocsResource::read()} handler (the SDK passes the requested `uri`
     * to the handler, so one method serves every doc). The manifest is
     * generated by `mcp:docs:sync`; if it hasn't been run yet we simply expose
     * no doc resources rather than failing to boot.
     */
    private function registerDocResources(): void
    {
        $manifestFile = __DIR__ . '/Resources/manifest.php';
        if (!is_file($manifestFile)) {
            return;
        }

        /** @var array<string, array{file: string, path: string, title: string, description: string}> $manifest */
        $manifest = require $manifestFile;

        foreach ($manifest as $uri => $meta) {
            // MCP resource names allow only [A-Za-z0-9_-] -- the doc's slash-
            // separated path (e.g. basics/routing) must be flattened.
            $name = preg_replace('/[^A-Za-z0-9_-]+/', '_', $meta['path']) ?? $meta['path'];

            $this->mcpResource(
                handlerFqcn: DocsResource::class,
                uri: $uri,
                method: 'read',
                name: $name,
                description: $meta['title'] . ($meta['description'] !== '' ? ' — ' . $meta['description'] : ''),
                mimeType: 'text/markdown',
            );
        }
    }

