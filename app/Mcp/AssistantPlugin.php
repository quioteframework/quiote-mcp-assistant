    /**
     * Reproduces the removed `PluginRegistrar::mcpTool()` -> `McpCatalog::addTool()` translation.
     *
     * @param array<string, mixed>|null $inputSchema
     * @param array<string, mixed>|null $outputSchema
     */
    private function mcpTool(
        string $handlerFqcn,
        ?string $method = null,
        ?string $name = null,
        ?string $description = null,
        ?array $inputSchema = null,
        ?array $outputSchema = null,
    ): void {
        McpCatalog::addTool(
            $method !== null ? [$handlerFqcn, $method] : $handlerFqcn,
            $name,
            null,
            $description,
            $inputSchema,
            $outputSchema,
        );
    }

    /** Reproduces the removed `PluginRegistrar::mcpResource()` -> `McpCatalog::addResource()` translation. */
    private function mcpResource(
        string $handlerFqcn,
        string $uri,
        ?string $method = null,
        ?string $name = null,
        ?string $description = null,
        ?string $mimeType = null,
    ): void {
        McpCatalog::addResource(
            $method !== null ? [$handlerFqcn, $method] : $handlerFqcn,
            $uri,
            $name,
            null,
            $description,
            $mimeType,
        );
    }

    /** Reproduces the removed `PluginRegistrar::mcpPrompt()` -> `McpCatalog::addPrompt()` translation. */
    private function mcpPrompt(
        string $handlerFqcn,
        ?string $method = null,
        ?string $name = null,
        ?string $description = null,
    ): void {
        McpCatalog::addPrompt(
            $method !== null ? [$handlerFqcn, $method] : $handlerFqcn,
            $name,
            null,
            $description,
        );
    }
}
